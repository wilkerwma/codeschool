from djcomponents import Widget


class Draggable(Widget):
    """
    jQuery UI Draggable element.

    Represents an object that can be dragged on the screen.
    """


class Droppable(Widget):
    """
    jQuery UI Draggable element.

    Represents an object that receives draggable objects.
    """


class Resizable(Widget):
    """
    jQuery UI resizable element.

    An element that can be resized by the user.
    """


class Selectable(Widget):
    """
    jQuery UI selectable element.

    A list of elements that support selection.
    """


class Sortable(Widget):
    """
    jQuery UI sortable element.

    A list of elements that can be sorted.
    """